=head1 DESCRIPTION

This is an alternative to C<Regexp::Common::*>. The main focus of
Regexp::Pattern::* is low startup overhead. There's a bridge module
L<Regexp::Common::RegexpPattern> to use patterns in C<Regexp::Pattern::*>
modules via Regexp::Common.


